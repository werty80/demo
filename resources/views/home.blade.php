                        2. Villages
                        The shinobi world consists of five great nations, each with its own Hidden Village.
                        Hidden Leaf Village (Konohagakure)*
                        - Country: Land of Fire
                        - Leaders: Hokage (e.g., Hashirama Senju, Minato Namikaze, Naruto Uzumaki)
                        - Notable Clans: Uchiha, Hyuga, Nara, Aburame, Uzumaki
                        - Description: Konoha is the most powerful ninja village and is known for producing legendary
                        shinobi like Naruto Uzumaki, Sasuke Uchiha, and Minato Namikaze (Fourth Hokage). The village is
                        home
                        to the Will of Fire, a set of ideals that prioritize unity among the people.

                        Hidden Sand Village (Sunagakure)
                        - Country: Land of Wind
                        - Leaders: Kazekage (e.g., Gaara of the Desert)
                        - Notable Shinobi: Gaara, Temari, Kankuro, Chiyo
                        - Description: Sunagakure is a desert-based village whose ninjas specialize in puppet techniques
                        and
                        wind-based jutsus. It is home to Gaara, the former Jinchūriki of the One-Tail (Shukaku).

                        3. Clans
                        Naruto’s world is driven by families or clans well-known for their unique abilities.
                        Uchiha Clan
                        - Background: A prestigious clan in Konohagakure, feared for their Sharingan and known for their
                        tragic clan massacre carried out by Itachi Uchiha.
                        - Abilities:
                        - Sharingan: A powerful dojutsu that grants heightened perception, Genjutsu, and replication of
                        ninjutsu.
                        - Mangekyo Sharingan: Grants advanced powers like Amaterasu and Susano’o.
                        - Eternal Mangekyo Sharingan: Enhanced, eternal vision without drawbacks.

                        - Key Members: Madara Uchiha, Itachi Uchiha, Sasuke Uchiha, Obito Uchiha.

                        Hyuga Clan
                        - Background: A noble clan in Konohagakure, famous for their Byakugan, which grants exceptional
                        vision and control over chakra.
                        - Abilities:
                        - Byakugan: Grants 360° vision and the ability to see chakra points.
                        - Gentle Fist: A taijutsu technique that targets an opponent’s chakra points with precision
                        strikes.

                        - Key Members: Hinata Hyuga, Neji Hyuga, Hiashi Hyuga.

                        4. Jutsus & Techniques
                        Ninjas use a variety of techniques, often classified into three main categories.
                        ### **Types of Jutsus**
                        1. Ninjutsu:
                        Techniques that manipulate chakra, such as Rasengan, Chidori, and Summoning Jutsu.
                        2. Genjutsu:
                        Illusionary techniques that disrupt the enemy’s perception. Examples: Tsukuyomi, Clone Jutsu.
                        3. Taijutsu:
                        Hand-to-hand combat techniques. Example: Rock Lee’s Eight Gates.
                        4. Kekkei Genkai (Bloodline Limits):
                        Unique abilities within clans. Example: Sharingan, Byakugan, Ice Release.

                        Iconic Jutsus
                        - Rasengan: A spinning ball of chakra created by Minato Namikaze, perfected by Naruto.
                        - Chidori: Lightning-based attack created by Kakashi Hatake and used by Sasuke.
                        - Amaterasu: Black flames that burn anything, cast through the Mangekyo Sharingan.

                        5. Tailed Beasts (Bijuu)
                        These are powerful creatures created by the Sage of Six Paths, each sealed inside a Jinchūriki.
                        Kurama (Nine-Tails)
                        - Jinchūriki:
                        - Past: Kushina Uzumaki, Minato Namikaze
                        - Current: Naruto Uzumaki

                        - Powers: Immense chakra reserves, Tailed Beast Bombs.
                        - Synopsis:
                        Kurama is known as the most powerful Tailed Beast, feared for its destructive power but later
                        becomes a friend and ally to Naruto.

